import moment from "moment";
import { useDispatch } from "react-redux";
import s from "./TodoForm.module.scss";
import { addTodo } from "../../redux/todo/todoOperations";

const initialFormState = {
  date: moment().format("YYYY-MM-DD"),
  descr: "",
  priority: "",
};

